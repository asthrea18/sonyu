// view 팝업 오픈
function review_detail_view_open(index_no,type){
	var obj = $(event.srcElement||event.target);
	var idx = obj.closest('li').index();

	$.ajax({
		url : '/review/review_view.php',
		type: 'post',
		data : {
			index_no : index_no
		},
		async: false,
		success: function(data){
			$('#popView').html(data);
			$('#popView').show();
			$('#popView .popslide').slick({
				arrows: true,
				dots: true,
				infinite: true,
				slidesToShow: 1,
				slidesToScroll: 1
			});
			if(type =='view_page'){
				$('#popView .popslide').slick('slickGoTo',idx);
			}
		}
	});
}

function review_help(index_no){
	var obj = $(event.srcElement||event.target);

	$.ajax({
		url : '/ajax_proc/review_help.php',
		type: 'post',
		data : {
			index_no : index_no
		},
		async: false,
		success: function(data){
			obj.closest('.cmt-state').find('.help-cnt').html(data);
			if(obj.hasClass('on')){
				obj.removeClass('on');
			}else{
				obj.addClass('on');
			}
		}
	});
}

