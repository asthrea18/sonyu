  나. 적립금 유효기간이 경과한 경우
  다. 적립금 사용 가능 조건 및 소멸에 대한 내용이 별도로 공지된 경우(그 기간을 사용기한으로 정함)
4. 적립금을 이용하여 부당 이득을 취하거나 악의적인 상거래가 발생할 경우, 해당 적립금의 지급을 중지 또는 회입할 수 있습니다.
5. 할인쿠폰은 현금으로 출금될 수 없으며 쿠폰에 표시된 유효기간이 만료되면 이용할 수 없습니다.
6. 할인쿠폰은 고객 사유로 인한 취소, 반품 시(단순변심 등)에는 재발행되지 않습니다.
7. 적립금, 쿠폰 부여 및 사용 등과 관련한 사항은 이 약관 또는 '몰'의 운영정책이 정한 바에 따릅니다.


