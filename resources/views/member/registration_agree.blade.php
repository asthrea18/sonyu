6. 이 약관에서 정하지 아니한 사항과 이 약관의 해석에 관하여는 「전자상거래 등에서의 소비자보호에 관한 법률」, 「약관의 규제에 관한 법률」, 공정거래위원회가 정하는 전자상거래등에서의 소비자 보호지침 및 관계법령 또는 상관례에 따릅니다.


제4조(서비스의 제공 및 변경)

1. '몰'은 다음과 같은 업무를 수행합니다.
  가. 재화 등에 대한 정보제공 및 수입대행계약 또는 구매계약의 체결
  나. 구매계약이 체결된 재화 등의 배송
  다. 기타 '몰'이 정하는 업무
2. '몰'은 재화 등의 품절 또는 기술적 사양의 변경 등의 경우에는 장차 체결되는 계약에 의해 제공할 재화 등의 내용을 변경할 수 있습니다. 이 경우에는 변경된 재화 등의 내용 및 현재의 재화 등의 내용을 게시한 곳에 즉시 공지합니다.
